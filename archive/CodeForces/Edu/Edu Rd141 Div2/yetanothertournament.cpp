int main() {
    ll t;
    cin >> t;
    while (t--) {
        ll n, m, rv = 0;
        cin >> n >> m;
        vector<ll> a(n);
        FOR (i, n) {
            cin >> a[i];
        }
        vector<ll> b = a;
        ssort(b);
        for (ll i = 0; i < n && b[i] <= m; i++) {
            m -= b[i];
            rv++;
        }
        if (rv != 0 && rv != n && m + b[rv - 1] >= a[rv]) {
            rv++;
        }
        cout << n + 1 - rv << '\n';
    }
    return 0;
}